void microDelay (uint16_t delay)
{
  __HAL_TIM_SET_COUNTER(&htim1, 0);
  while (__HAL_TIM_GET_COUNTER(&htim1) < delay);
}

void motorX(int direction)  //bottom motor
{
	HAL_GPIO_WritePin(DIR_PORTB, DIR_PIN, direction);
	HAL_GPIO_WritePin(STEP_PORTB, STEP_PIN, GPIO_PIN_SET);
   microDelay(stepDelay);
   HAL_GPIO_WritePin(STEP_PORTB, STEP_PIN, GPIO_PIN_RESET);
   microDelay(stepDelay);
}

